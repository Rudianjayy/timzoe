                                    <h1 class="post-title">PEMUDA ISLAM HARUS MILIKI PERAN DI ERA DIGITAL</h1>
                                    <p>Pemuda Islam harus miliki peran di era digital. Dan satu hal yang dapat
                                        menjembatani pemuda dengan era digital ialah kesadaran.
                                        Hal itu disampaikan oleh Waka Ismuba SMK Muhammadiyah 1 (Muhi) Genteng Ustadz
                                        Drei Herba T saat menjadi pemateri pada Pengajian Rutin Masjid An Namiroh SMK
                                        Muhi Genteng Banyuwangi, Jumat (28/01/2022).
                                        Pada kajian kali ini Takmir Masjid An Namiroh mengangkat tema Wajah Baru Pemuda
                                        Islam di Era Digital. Ada dua point yang dijabarkan dalam tema kali ini, yakni
                                        bagaimana dunia digital itu sendiri dan bagaimana ruang lingkup Islam.
                                        Menurut Drei Herba seorang pemuda haruslah memiliki peranan dalam era digital
                                        sebagai bentuk kesadaran dalam diri generasi muda saat ini.</p>
                                    <br>
                                    <br>
                                    <strong>Kembangkan Ilmu Ajaran Islam</strong>
                                    <p>Dia kemudian mengutip ungkapan Ali bin Abi Thalib yang menyatakan al haqqu bila
                                        nidzam sayuglibuhul bathil bin an nidzam. Maknanya bahwa kebenaran yang tidak
                                        terorganisir secara bersama maka akan kalah dengan keburukan.
                                        “Maka pemuda memang harus memiliki pondasi yang kuat untuk menghadapi era
                                        digital. Hadist dan al-Quran memanglah sebagai pedoman hidup bagi kita umat
                                        islam. Dan era digital adalah bagian dari pemuda itu tumbuh,” ujarnya.
                                        Tak bisa dipungkiri, lanjutnya, beberapa aktivitas yang kita lakukan memang
                                        tidak lepas dengan namanya media. “Sebagai generasi muda, kita tidak bisa
                                        menghindari hal tersebut. Pemuda harus berperan di dalamnya untuk terus
                                        mengembangkan ilmu ajaran Islam melalui digitalisasi,” ungkapnya.
                                        Kombinasi Pengetahuan dan Akhlak
                                        Banyak para remaja, sambungnya, yang memiliki ilmu atau kepandaian. Namun hal
                                        tersebut tidaklah cukup. Pemuda juga harus memiliki akhlak yang baik.
                                        “Maka era digital haruslah kita gencarkan, sebagai bentuk pengaplikasian
                                        pengetahuan yang kita miliki dengan akhlak yang kita miliki. Karena pemuda Islam
                                        juga perlu adanya sebuah landasan,” pesannya.
                                        Dia berharap pemuda Islam yang memiliki pengetahuan dan juga akhlak dapat
                                        diterjemahkan atau diliterasikan dalam dunia digital.
                                        “Saya juga berharap kajian seperti ini akan terus berlangsung setiap bulannya.
                                        Untuk terus mengembangkan ilmu serta pengetahuan bagi seluruh siswa siswi SMK
                                        Muhi Genteng,” tuturnya dalam kajian yang dihadiri kurang lebih dua puluh
                                        peserta. (*)

                                        Penulis Prasiska Leony. Co-Editor Sugiran. Editor Mohammad Nurfatoni.</p>

